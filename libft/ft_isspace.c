#include "libft.h"

int	ft_isspace(char *s)
{
	while (*s)
	{
		if (*s == ' ' || *s == '\n' || *s == '\t' \
			|| *s == '\r' || *s == 'v' || *s == '\f')
			return (1);
		s++;
	}
	return (0);
}
